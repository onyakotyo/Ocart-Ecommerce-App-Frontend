export const createOrder = (order) => async (dispatch, getState) => {

   console.log('DATA CREATE ORDER',order)
  try {
    dispatch({
      type: ORDER_CREATE_REQUEST,
    })

    const {
      userLogin: { userInfo },
    } = getState()

    const config = {
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${userInfo.token}`,
      },
    }

     console.log('MAIL CREATE ORDER1',order)

   

    // const { data } = await axios.post(`/api/orders`, order, config)

    // const { data } = await axios.post(`/api/mail`, order, config)

  const [dataOrders, dataMail] = await Promise.all([axios.post('/api/orders', order, config), axios.post('/api/mail', order, config)])

    console.log('MAIL-DATA-ORDERS',dataOrders)

    console.log('MAIL-DATA-MAIL',dataMail)

    dispatch({
      type: ORDER_CREATE_SUCCESS,
      payload: dataOrders,
    })

     dispatch({
      type: ORDER_CREATE_SUCCESS,
      payload: dataMail,
    })

    dispatch({
      type: CART_CLEAR_ITEMS,
      payload: dataOrders,
    })

     dispatch({
      type: CART_CLEAR_ITEMS,
      payload: dataMail,
    })

    // dispatch({
    //   type: CART_REMOVE_ALL_ITEMS

    // })

    localStorage.removeItem('cartItems')
  } catch (error) {
    const message =
      error.response && error.response.data.message
        ? error.response.data.message
        : error.message
    if (message === 'Not authorized, token failed') {
      dispatch(logout())
    }
    dispatch({
      type: ORDER_CREATE_FAIL,
      payload: message,
    })
  }
}

